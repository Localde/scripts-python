variavel = input('Digite algo: ')

print('é espaço: {}'.format(variavel.isspace()))
print('é numero: {}'.format(variavel.isnumeric()))
print('é minusculo: {}'.format(variavel.islower()))
print('é maiusculo: {}'.format(variavel.isupper()))
print('é titulo: {}'.format(variavel.istitle()))
print('é letra: {}'.format(variavel.isalpha()))
